function recv(src, event, message)
    if nargin < 3
        message = 'Received data on ';
    end
    fprintf(['%sinterface %s:\n', ...
        '\tfrom %s:%d @ %s; DatagramLength %d bytes;\n>>>Data:\n%s\n>>>end of data\n'], ...
        message, src.Name, event.Data.DatagramAddress, event.Data.DatagramPort, ...
        datestr(event.Data.AbsTime), event.Data.DatagramLength, ...
        fread(src, event.Data.DatagramLength));
end